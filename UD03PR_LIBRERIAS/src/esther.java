public class esther {
public static double areaCircunferencia(double r) {
	
	double area;
	area=Math.PI * r * r;
	return area;
}


public static double diametroCircunferencia(double r) {
	double diametro;
	diametro=2*r;
	return diametro;
	
}

public static double perimetroCircunferencia(double r) {
	
	double perimetro;
	perimetro =2 * Math.PI * r;
	return perimetro;
}
}
